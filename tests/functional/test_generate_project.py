@pytest.fixture
def postgres_up(generated_project, docker_daemon_required):
    """Bring up only postgres (--wait), yield, and always tear down."""
    out, params = generated_project
    # config is a precondition: never attempt `up` on a file that won't parse.
    cfg = _compose(out, "config", "-q")
    if cfg.returncode != 0:
        pytest.fail(f"docker compose config failed:\n{cfg.stderr}")

    up = _compose(
        out, "up", "-d", "--wait", "--wait-timeout", "180", "postgres"
    )
    try:
        yield out, params, up
    finally:
        _compose(out, "down", "-v")


@pytest.mark.needs_docker
def test_postgres_service_comes_up_healthy(postgres_up):
    out, _params, up = postgres_up

    # `up --wait` returns 0 only once postgres reports healthy.
    assert up.returncode == 0, f"up failed:\n{up.stdout}\n{up.stderr}"

    ps = _compose(out, "ps", "--format", "json", "postgres")
    entries = [
        json.loads(line) for line in ps.stdout.splitlines() if line.strip()
    ]
    postgres = next(e for e in entries if e.get("Service") == "postgres")
    assert postgres.get("State") == "running"
    if postgres.get("Health"):  # healthcheck is defined, so this is present
        assert postgres["Health"] == "healthy"


@pytest.mark.needs_docker
def test_postgres_init_creates_databases_and_roles(postgres_up):
    # The generated postgres/config/init.sh (AUTO_CREATE_DATABASE defaults on)
    # runs on the fresh volume and creates a database + like-named role per
    # configured DB name. One round trip lists both catalogs. The stack uses
    # scram-sha-256 for every connection (including the socket), so feed psql
    # the generated superuser password via PGPASSWORD.
    out, params, _up = postgres_up
    password = _read(out, ".secrets", "postgres_password.gen")
    probe = (
        "SELECT 'db:' || datname FROM pg_database "
        "UNION ALL "
        "SELECT 'role:' || rolname FROM pg_roles"
    )

    result = _compose(
        out,
        "exec",
        "-T",
        "-e",
        f"PGPASSWORD={password}",
        "postgres",
        "psql",
        "-U",
        "postgres",
        "-d",
        "postgres",
        "-tAc",
        probe,
    )

    assert result.returncode == 0, result.stderr
    catalog = {
        line.strip() for line in result.stdout.splitlines() if line.strip()
    }
    expected = {
        f"db:{params['agui_db']}",
        f"db:{params['authz_db']}",
        "db:soliplex_ingester",
        f"role:{params['agui_db']}",
        f"role:{params['authz_db']}",
        "role:soliplex_ingester",
    }
    assert expected <= catalog, sorted(expected - catalog)

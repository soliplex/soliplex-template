def _compose(cwd, *args):
    """Run ``docker compose <args>`` in ``cwd``; return its result."""
    return subprocess.run(
        ["docker", "compose", *args],
        cwd=cwd,
        capture_output=True,
        text=True,
    )


def _read(out, *parts):
    return out.joinpath(*parts).read_text(encoding="utf-8")


